// @lc code=start
class Solution {
public:
    int climbStairs(int n) {
        if (n <= 1) return n;
        int dp[3];
        // 只需要维护三个数值就可以了 ,不需要记录整个序列
        // vector<int> dp(n + 1);
        dp[1] = 1;
        dp[2] = 2;
        for (int i = 3; i <= n; i++) {
            int sum = dp[1] + dp[2];
            dp[1] = dp[2];
            dp[2] = sum;
            // dp[i] = dp[i - 1] + dp[i - 2];
        }
        return dp[2];
        // return dp[n];
    }
};
